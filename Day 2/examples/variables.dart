void main() {
  // String variable
  String name = "Yash";

  // Integer variable
  int age = 26;

  // Double variable
  double height = 5.9;

  // Boolean variable
  bool isDeveloper = true;

  // Dynamic variable (type can change)
  dynamic value = "Hello";
  value = 100;

  // Var keyword (Dart automatically detects type)
  var city = "Mumbai";

  // Final variable (can only be assigned once)
  final country = "India";

  // Const variable (compile-time constant)
  const pi = 3.14159;

  // Printing variables
  print("Name: $name");
  print("Age: $age");
  print("Height: $height");
  print("Is Developer: $isDeveloper");
  print("Dynamic Value: $value");
  print("City: $city");
  print("Country: $country");
  print("PI Value: $pi");
}
